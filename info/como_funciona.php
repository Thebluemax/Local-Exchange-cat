<p>Els diners de temps és una nova moneda per a proporcionar una solució a les retallades massius de la despesa pública en matèria de benestar social, però sense recórrer a la intervenció estatal de l'economia, sinó al contrari, a partir de la llibertat d'acció econòmica.</p>
